package com.generator.model;

import com.generator.model.enums.AttributeType;
import com.generator.model.enums.GenerationType;

import jakarta.xml.bind.annotation.XmlAttribute;

public class Attribute {
	@XmlAttribute
	private String name;
	@XmlAttribute
	private AttributeType attributeType;
	@XmlAttribute(required = false)
	private GenerationType generationType;
	@XmlAttribute(required = false)
	private Enum<?> enumType;
	@XmlAttribute(required = false)
	private boolean nullable;
	@XmlAttribute(required = false)
	private boolean unique;
	@XmlAttribute(required = false)
	private boolean autoGenerated;

	public Attribute() {
	}

	public Attribute(String name, AttributeType attributeType, boolean nullable, boolean autoGenerated) {
		super();
		this.name = name;
		this.attributeType = attributeType;
		this.nullable = nullable;
		this.autoGenerated = autoGenerated;
		this.generationType = GenerationType.AUTO;
		unique = false;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public AttributeType getAttributeType() {
		return attributeType;
	}

	public void setAttributeType(AttributeType attributeType) {
		this.attributeType = attributeType;
	}

	public boolean isNullable() {
		return nullable;
	}

	public void setNullable(boolean nullable) {
		this.nullable = nullable;
	}

	public boolean isAutoGenerated() {
		return autoGenerated;
	}

	public void setAutoGenerated(boolean autoGenerated) {
		this.autoGenerated = autoGenerated;
	}

	public boolean isUnique() {
		return unique;
	}

	public void setUnique(boolean unique) {
		this.unique = unique;
	}

	public GenerationType getGenerationType() {
		return generationType;
	}

	public void setGenerationType(GenerationType generationType) {
		this.generationType = generationType;
	}

	public Enum<?> getEnumType() {
		return enumType;
	}

	public void setEnumType(Enum<?> enumType) {
		this.enumType = enumType;
	}
}
